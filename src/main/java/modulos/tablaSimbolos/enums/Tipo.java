package modulos.tablaSimbolos.enums;

/**
 * Enumeración que define los posibles tipos en una tabla de símbolos.
 * Estos tipos representan los diferentes tipos de datos y constructos que
 * pueden aparecer en un lenguaje de programación.
 */
public enum Tipo {
    BOOLEAN,
    FUNCTION,
    INT,
    STRING,
    VOID,
    OK // Tipo utilizado para indicar que una operación fue exitosa
}
